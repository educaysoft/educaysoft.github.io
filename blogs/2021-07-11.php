<p  style="font-family: 'Alex Brush';font-size: 30px; padding-top:10px; text-align:right;"  >Ing. Stalin Francis</p>
        <p>Los estudios de postgrado realizados a nivel de maestría, es un proceso de aprendizaje que se constituye en parte de la formación continua del profesional, proceso que empezo con la formación inicial, se refina después de la inserción al servicio laboral y se amplia y desarrolla a lo largo de toda la vida.  </p>
<p> La educación continua para el desarrollo profesional ha sido creada pensando en el sujeto que debe estar actualizado con los cambios que continuamente se dan en la profesión, y que debe estar reflejado en su curriculo.</p>

<p>Es importante que los profesionales comprendan que el desarrollo profesional, necesita la ejecución de procesos que contemplan experiencias y reflexiones sistemáticas sobre la práctica profesional. Estos procesos incluye actividades formales y informales que se distinguen de la simple capacitación y entrenamiento en breves cursos y talleres donde solo se abordan algunos aspecto especificos de la práctica profesional aplicable a corto plazo.</p>
<p> El enfoque de postgrado a nivel de maestría profesionalizante es relativamente novedoso para el desarrollo profesional y ha sido concebido como un cambio de paradigma en su definición.</p>
<p> En el postgrado que ofrece la UTLTE, el componente de la  investigación es clave para lograr un verdadero desarrollo profesional, y debe realizarse de menera metódica aproximandose lo más posible a la realidad concreta, a fin de generar información que posibilite una efeciente toma de decisiones que resuelva de forma oportuna problemas concretos y reales.</p>   
<p> Todas las investigación realizadas en la maestría de Tecnología de la Información para otorgamiento del grado, debe ofrecer resultados innovadores en lo referente a la creación  o innovación del conocimiento y la tecnología, siempre en beneficio de la sociedad.</p>
<p> La investigación a nivel de maestría debe promover un alto nivel de actividad intelectual alimentada de profundos procesos experimentales que den como resultado conocimiento innovadores sobre una temática específica que permitan craer materiales, producto, procesos, procedimeinto, metodologías o tecnologías de gran utilidad.</p>

<p> La investigación final en la maestría, sera propuesto como  un proyecto de tesis, que consistirá en un plan general conformado por una serie de fases cuidadosamente estructuradas para guiar al maestrante hasta la culminación esta.</p> 
            
<p> La dirección de Maestría de la Universidad Técnica Luis Vargas Torres de Esmeraldas, ha conformado una Comisión Académica de tutores y/o directores de la Maestría para concebir y referenciar los formatos de investigación de trabajos de titulación institucionales que medien científicamente la construcción de conocimientos que tributen al desarrollo de la ciencia.</p>
<p>
El diseño de la maestría en Tecnología de la Información aprobado por el CES con número RPC-SO-12-NO.232-2020 el 6 de mayo del 2020, cuenta con tres modalidades de trabajo de titulación que son:</p>
 
<ul>
<li> Estudios comparados complejos </li>
<li> Propuestas metodológicas y tecnológicas avanzadas</li>
<li> Proyectos de titulación con componentes de investigación aplicada y/o desarrollo</li>
</ul>

<p> Este material, llamado <b>"Protocolo para la Elaboración de Trabajos de Titulación en el Postgrado"</b>, ha sido el resultado del trabajo académico y científico de la Comisión  Académica de Tutores y/o Directores de Maestrías, y constituye un aporte importante que norma la producción científica de los estudiantes de maestría. Se concibe como un documento orientador dirigido a los estudiantes que se preparan para iniciar su proyectos de investigación en los programas de maestrías de la UTLVTE.</p>

<p> Teniendo en cuenta este antecendete en el resto de este documento se incluira la información detallada sobre las etapas que deben conocer y desarrollar los maestrantes para estar idoneos en la elaboración del proyecto y del informe final, los documentos que se deben elaborar los cuales deben cumplir con el formato académico institución establecido en el reglamento de régimen académico de postgrado.</p>

<p> De forma general las características básicas que deben tener dichos documentos son: </p>

<ul>
<li> <b>El alcance</b> que constituye el número de página y particularidade a cumplir en cada una de las secciones que se construiran para exponer el proceso investigativo desarrollado.</li>
<li>  Que el documento escrito se suscriba a la convenciones propias de cada una de als formas de titulación seleccionada, hay un instrumento que validará el trabajo de titulación el cual servirá como instrumento de evaluación a ser utilizado por un tribunal nombrado para este efecto.</li>
<li> El Trabajo de Titulación final de máster debe ser un texto académico donde no puede faltar la constancia de revisiones bibliográfica, Objetivos, contexto tecnológico científico, justificación dela problemática seleccionada, marco teórico y metodológico. </li>
<li> Como punto final, la culminación del proceso de Titulación implica la defensa oral de la propuesta frente a un tribunal compuesto por tres profesores designados por la coordinación del Máster.</li>
</ul>
         
